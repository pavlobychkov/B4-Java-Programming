package day06_b_unary_operators.day06_a_arithmetic_operators;

public class IncromentExample {

    public static void main (String [] args){

        int y = 5;
        System.out.println(y);
        y = -y;
        System.out.println(y);

        int x = 0;
        System.out.println(x);
        x = x + 1;
        System.out.println(x);
        ++x;
        System.out.println(x);
        x++;
        System.out.println(x);;
        System.out.println("---------------");
        System.out.println(++x);
        System.out.println(x);
        System.out.println(x++);
        System.out.println(x);



    }
}
