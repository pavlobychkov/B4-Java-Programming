package day06_b_unary_operators.day06_a_arithmetic_operators;

public class DifferentDataTypes {

    public static void main (String [] args){

        System.out.println(10 - 5);
        System.out.println(10 * 5);
        System.out.println(10.0 / 5);
        System.out.println(10.0 / 2.0);
        System.out.println(10 / 2.0);
        System.out.println((int)10.5 / 5.0);
        System.out.println(10.5 / 5.0);
        System.out.println((int)10.5 / (int)5.0);

    }
}
